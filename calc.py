num1 = int(input("Digite o primeiro número inteiro: "))
num2 = int(input("Digite o segundo número inteiro: "))
num_real = float(input("Digite o número real: "))

resultado_a = (2 * num1) * (num2 / 2)
print("Resultado a): O produto do dobro do primeiro com metade do segundo é", resultado_a)

resultado_b = (3 * num1) + num_real
print("Resultado b): A soma do triplo do primeiro com o terceiro é", resultado_b)

resultado_c = num_real ** 3
print("Resultado c): O terceiro elevado ao cubo é", resultado_c)
